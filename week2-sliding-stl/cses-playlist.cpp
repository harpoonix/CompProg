void solve() {
    int n; cin >> n;
    int k[n];
    for (int i=0; i<n; i++) {
        cin >> k[i];
    }
    set<int> unique; int cardinality = 0, r = 0;
    for (int l=0; l<n; l++) {
        while (r < n && !unique.count(k[r])){
            unique.insert(k[r++]);
        }
        cardinality = max(cardinality, r - l);
        unique.erase(k[l]);
    }
    cout << cardinality << "\n";
}

