void solve() {
    int n, k; cin >> n >> k;
    char s[n + 1];
    cin.ignore();
    cin.getline(s, n + 1);
    vi vowels;
    for (int i=0; i<n+1; i++) {
        if (s[i] == 'a' || s[i] == 'e' || s[i] == 'i' || s[i] == 'o' || s[i] == 'u'){
            vowels.push_back(i);
        }
    }
    ll slices = 1; int options;
    for (int i=k; i<vowels.size(); i+=k) {
        options = vowels[i] - vowels[i-1];
        slices = slices * ll(options);
        slices = slices%(1000000007);
    }
    cout << slices << "\n";
}

